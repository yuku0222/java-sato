class SumAve1 {
    public static void main(String[] args) {
        int x;
        int y;

        x = 63;
        y = 18;

        System.out.println("xの値は" + x + "です。");
        System.out.println("yの値は" + y + "です。");
        System.out.println("合計は" + (x + y) + "です。");
        System.out.println("平均は" + (x + y) / 2 + "です。");
    }
}
